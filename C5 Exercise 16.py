from math import sin
from math import cos
from math import tan
from math import radians

angle = radians(int(input('Enter an angle in degrees:')))

sine = sin(angle)
cosine = cos(angle)
tangent = tan(angle)

print (' ')
print ('Sine: %.2f' %(sine))
print ('Cosine: %.2f' %(cosine))
print ('Tangent: %.2f' %(tangent))
